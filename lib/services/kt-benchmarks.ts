/**
 * Evaluates KT performance using dialogue-based datasets.
 * Krivich et al. (2025): shift to dialogue-based benchmarks requires tracing
 * knowledge from *quality* of student questions and natural language answers.
 */
export class DialogueKTEvaluator {
  /**
   * Compute AUC approximation (Wilcoxon-Mann-Whitney U statistic)
   * for a set of (predicted mastery, actual correct) pairs.
   */
  static computeAUC(predictions: Array<{ predicted: number; actual: boolean }>): number {
    if (predictions.length === 0) return 0.5;
    const positives = predictions.filter((p) => p.actual);
    const negatives = predictions.filter((p) => !p.actual);

    if (positives.length === 0 || negatives.length === 0) return 0.5;

    let concordant = 0;
    let tied = 0;
    for (const pos of positives) {
      for (const neg of negatives) {
        if (pos.predicted > neg.predicted) concordant++;
        else if (pos.predicted === neg.predicted) tied++;
      }
    }

    return (concordant + tied * 0.5) / (positives.length * negatives.length);
  }

  /**
   * Evaluate a complete KT session against ground-truth quiz results.
   */
  static evaluateSession(
    sessionId: string,
    tracedKCs: KnowledgeComponent[],
    quizResults: Array<{ kcId: string; correct: boolean }>
  ): DialogueKTEvaluation {
    const predictions = quizResults.map((q) => {
      const kc = tracedKCs.find((k) => k.id === q.kcId);
      return { predicted: kc?.masteryEstimate ?? 0.5, actual: q.correct };
    });

    const predictedAccuracy =
      predictions.reduce((s, p) => s + p.predicted, 0) / Math.max(1, predictions.length);
    const actualAccuracy =
      predictions.filter((p) => p.actual).length / Math.max(1, predictions.length);
    const aucScore = this.computeAUC(predictions);

    // Alignment scores per KC
    const alignmentScores: KTAlignmentScore[] = quizResults.map((q) => {
      const kc = tracedKCs.find((k) => k.id === q.kcId);
      const ktMastery = kc?.masteryEstimate ?? 0.5;
      const actualMastery = q.correct ? 0.9 : 0.1;
      return {
        ktPredictedMastery: ktMastery,
        llmExplainedMastery: actualMastery,
        alignmentScore: Math.max(0, 1 - Math.abs(ktMastery - actualMastery)),
        discrepancyFlag: Math.abs(ktMastery - actualMastery) > 0.3,
      };
    });

    return { sessionId, tracedKCs, predictedAccuracy, actualAccuracy, aucScore, alignmentScores };
  }
}

