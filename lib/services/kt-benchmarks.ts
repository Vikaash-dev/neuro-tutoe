/**
 * Graph-based benchmark validation.
 * 2025 benchmarks focus on validating Knowledge Graphs generated by LLMs —
 * crucial for Structure-Augmented hybrid models.
 */
export class KnowledgeGraphValidator {
  /**
   * Validate that all edges in the graph refer to existing node IDs.
   * Returns a list of orphaned (dangling) edges.
   */
  static validateEdgeIntegrity(
    nodes: KGNode[],
    edges: KGEdge[]
  ): { valid: boolean; orphanedEdges: KGEdge[] } {
    const nodeIds = new Set(nodes.map((n) => n.id));
    const orphanedEdges = edges.filter(
      (e) => !nodeIds.has(e.sourceId) || !nodeIds.has(e.targetId)
    );
    return { valid: orphanedEdges.length === 0, orphanedEdges };
  }

  /**
   * Detect cycles in the prerequisite graph (prerequisite_of edges).
   * Cycles indicate an invalid curriculum structure.
   */
  static detectPrerequisiteCycles(nodes: KGNode[], edges: KGEdge[]): string[][] {
    const prereqEdges = edges.filter((e) => e.relationshipType === "prerequisite_of");
    const adj = new Map<string, string[]>();
    for (const node of nodes) adj.set(node.id, []);
    for (const edge of prereqEdges) {
      adj.get(edge.sourceId)?.push(edge.targetId);
    }

    const visited = new Set<string>();
    const recursionStack = new Set<string>();
    const cycles: string[][] = [];

    const dfs = (nodeId: string, path: string[]): void => {
      visited.add(nodeId);
      recursionStack.add(nodeId);

      for (const neighbour of adj.get(nodeId) ?? []) {
        if (!visited.has(neighbour)) {
          dfs(neighbour, [...path, neighbour]);
        } else if (recursionStack.has(neighbour)) {
          const cycleStart = path.indexOf(neighbour);
          cycles.push(path.slice(cycleStart));
        }
      }
      recursionStack.delete(nodeId);
    };

    for (const node of nodes) {
      if (!visited.has(node.id)) dfs(node.id, [node.id]);
    }
    return cycles;
  }

  /**
   * Compute graph coverage: proportion of concept nodes reachable from root nodes.
   * Low coverage means isolated nodes that cannot be reached by curriculum traversal.
   */
  static computeCoverage(nodes: KGNode[], edges: KGEdge[]): number {
    if (nodes.length === 0) return 0;
    const conceptNodes = nodes.filter((n) => n.type === "concept");
    if (conceptNodes.length === 0) return 0;

    const adj = new Map<string, string[]>();
    for (const node of conceptNodes) adj.set(node.id, []);
    for (const edge of edges) {
      if (adj.has(edge.sourceId)) adj.get(edge.sourceId)!.push(edge.targetId);
    }

    // BFS from nodes with no incoming prerequisite edges (root nodes)
    const inDegree = new Map<string, number>();
    for (const node of conceptNodes) inDegree.set(node.id, 0);
    for (const edge of edges.filter((e) => e.relationshipType === "prerequisite_of")) {
      inDegree.set(edge.targetId, (inDegree.get(edge.targetId) ?? 0) + 1);
    }

    const roots = [...inDegree.entries()].filter(([, d]) => d === 0).map(([id]) => id);
    const reachable = new Set<string>();
    const queue = [...roots];
    while (queue.length > 0) {
      const current = queue.shift()!;
      reachable.add(current);
      for (const neighbour of adj.get(current) ?? []) {
        if (!reachable.has(neighbour)) queue.push(neighbour);
      }
    }

    return reachable.size / conceptNodes.length;
  }

  /**
   * Compute a composite graph quality score (0-1).
   */
  static computeGraphQuality(nodes: KGNode[], edges: KGEdge[]): number {
    const { valid } = this.validateEdgeIntegrity(nodes, edges);
    const cycles = this.detectPrerequisiteCycles(nodes, edges);
    const coverage = this.computeCoverage(nodes, edges);

    const integrityScore = valid ? 1 : 0.5;
    const acyclicScore = cycles.length === 0 ? 1 : Math.max(0, 1 - cycles.length * 0.1);
    return (integrityScore * 0.3 + acyclicScore * 0.3 + coverage * 0.4);
  }
}

