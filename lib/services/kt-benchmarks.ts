/**
 * EDM 2025: Alignment Scores measure how well an LLM's generated explanation
 * matches the inferred knowledge state from a KT model.
 * Moves beyond simple AUC/ACC to assess explanation quality.
 */
export class AlignmentScoreCalculator {
  /**
   * Compute alignment between a KT model's mastery prediction and the
   * mastery level implied by the LLM's explanation text.
   *
   * @param ktMastery    0-1, from BKT/DKT model
   * @param explanation  LLM-generated explanation or response
   */
  static computeFromText(ktMastery: number, explanation: string): KTAlignmentScore {
    const llmMastery = this.inferMasteryFromText(explanation);
    return this.computeDirect(ktMastery, llmMastery);
  }

  /**
   * Direct computation when both mastery values are known.
   */
  static computeDirect(ktMastery: number, llmMastery: number): KTAlignmentScore {
    const alignmentScore = Math.max(0, 1 - Math.abs(ktMastery - llmMastery));
    return {
      ktPredictedMastery: ktMastery,
      llmExplainedMastery: llmMastery,
      alignmentScore,
      discrepancyFlag: Math.abs(ktMastery - llmMastery) > 0.3,
    };
  }

  /**
   * Infer implied mastery level from explanation text using linguistic signals.
   * High-mastery signals: correct technical vocabulary, confident phrasing.
   * Low-mastery signals: hedging language, errors, shallow descriptions.
   */
  static inferMasteryFromText(explanation: string): number {
    const lower = explanation.toLowerCase();
    const words = lower.split(/\s+/);

    // High-mastery markers
    const masteryMarkers = ["because", "therefore", "specifically", "precisely", "furthermore", "demonstrated", "applied", "proved", "derived"];
    const masteryCount = masteryMarkers.filter((m) => lower.includes(m)).length;

    // Low-mastery / uncertainty markers
    const uncertaintyMarkers = ["maybe", "perhaps", "i think", "not sure", "might be", "possibly", "i don't know", "i'm not", "confused"];
    const uncertaintyCount = uncertaintyMarkers.filter((m) => lower.includes(m)).length;

    // Lexical richness as a proxy for depth of explanation
    const uniqueWords = new Set(words);
    const ttr = uniqueWords.size / Math.max(1, words.length);

    const baseScore = 0.5;
    const masteryBonus = Math.min(0.3, masteryCount * 0.06);
    const uncertaintyPenalty = Math.min(0.3, uncertaintyCount * 0.08);
    const ttrBonus = (ttr - 0.4) * 0.3;

    return Math.min(1, Math.max(0, baseScore + masteryBonus - uncertaintyPenalty + ttrBonus));
  }

  /**
   * Batch compute alignment scores for a session.
   */
  static batchCompute(
    pairs: Array<{ ktMastery: number; llmMastery: number }>
  ): {
    scores: KTAlignmentScore[];
    meanAlignment: number;
    discrepancyRate: number;
  } {
    const scores = pairs.map((p) => this.computeDirect(p.ktMastery, p.llmMastery));
    const meanAlignment = scores.reduce((s, sc) => s + sc.alignmentScore, 0) / Math.max(1, scores.length);
    const discrepancyRate = scores.filter((s) => s.discrepancyFlag).length / Math.max(1, scores.length);
    return { scores, meanAlignment, discrepancyRate };
  }
}

