/**
 * Responsible AI metrics for Knowledge Tracing.
 * Krivich et al. (2025): fairness, transparency, and calibration are becoming
 * standard requirements for new KT models.
 */
export class ResponsibleAIEvaluator {
  /**
   * Compute demographic parity gap across student groups.
   * Lower is fairer — maximum accuracy deviation across demographic groups.
   *
   * @param groupPredictions  Map of group label → (predicted, actual) pairs
   */
  static computeDemographicParityGap(
    groupPredictions: Map<string, Array<{ predicted: number; actual: boolean }>>
  ): number {
    const groupAccuracies: number[] = [];

    for (const [, predictions] of groupPredictions) {
      if (predictions.length === 0) continue;
      const correct = predictions.filter(
        (p) => (p.predicted >= 0.5) === p.actual
      ).length;
      groupAccuracies.push(correct / predictions.length);
    }

    if (groupAccuracies.length < 2) return 0;
    const maxAcc = Math.max(...groupAccuracies);
    const minAcc = Math.min(...groupAccuracies);
    return maxAcc - minAcc;
  }

  /**
   * Compute calibration error: how close predicted probabilities are to actual accuracy.
   * Uses Expected Calibration Error (ECE) with 10 bins.
   */
  static computeCalibrationError(
    predictions: Array<{ predicted: number; actual: boolean }>
  ): number {
    if (predictions.length === 0) return 0;
    const bins = 10;
    let totalError = 0;

    for (let b = 0; b < bins; b++) {
      const lower = b / bins;
      const upper = (b + 1) / bins;
      const binPreds = predictions.filter(
        (p) => p.predicted >= lower && (b === bins - 1 ? p.predicted <= upper : p.predicted < upper)
      );
      if (binPreds.length === 0) continue;
      const avgPredicted = binPreds.reduce((s, p) => s + p.predicted, 0) / binPreds.length;
      const avgActual = binPreds.filter((p) => p.actual).length / binPreds.length;
      totalError += (binPreds.length / predictions.length) * Math.abs(avgPredicted - avgActual);
    }

    return totalError;
  }

  /**
   * Compute full Responsible AI metrics for a KT system evaluation.
   *
   * @param predictions          All (predicted, actual) pairs
   * @param groupPredictions     Same pairs split by demographic group
   * @param explainedDecisions   Number of KT decisions with human-readable explanation
   * @param totalDecisions       Total number of KT decisions
   */
  static evaluate(
    predictions: Array<{ predicted: number; actual: boolean }>,
    groupPredictions: Map<string, Array<{ predicted: number; actual: boolean }>>,
    explainedDecisions: number,
    totalDecisions: number
  ): ResponsibleAIMetrics {
    return {
      demographicParityGap: this.computeDemographicParityGap(groupPredictions),
      transparencyRate: totalDecisions > 0 ? explainedDecisions / totalDecisions : 0,
      calibrationError: this.computeCalibrationError(predictions),
      evaluatedAt: Date.now(),
    };
  }
}
